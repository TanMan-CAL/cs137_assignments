int dfs(int i, int j, int m, int n, int a[m][n]){
    if ((i > m-1) || (j > n-1) || (j<0) || (i<0) || (a[i][j]==0)){
        return 0;
    }
    
    if (i==(m-1) && (j==(n-1))){
        return 1;
    }

    return dfs(i+1, j, m, n, a) + dfs(i, j+1, m, n, a);
}

int monotonePath(int m, int n, int a[m][n]) {
    int solves = dfs(0, 0, m, n, a);
    if (solves != 0) {
        return 1;
    }
    return 0;
}



